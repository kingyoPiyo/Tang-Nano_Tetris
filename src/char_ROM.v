                6'd26 : o_data <= 64'h70888888A8906800; // Q
                6'd27 : o_data <= 64'hF08888F0A0908800; // R
                6'd28 : o_data <= 64'h788080700808F000; // S
                6'd29 : o_data <= 64'hF820202020202000; // T

module char_rom (
    input   wire            i_clk,
    input   wire            i_res_n,
    input   wire    [ 5:0]  i_addr,
    output  reg     [63:0]  o_data
    )/* synthesis syn_romstyle = "block_rom" */;

    always @(posedge i_clk or negedge i_res_n) begin
        if (~i_res_n) begin
            o_data <= 64'd0;
        end else begin
            case (i_addr[5:0])
                6'd0  : o_data <= 64'h708898A8C8887000; // 0
                6'd1  : o_data <= 64'h2060202020207000; // 1
